/** Get all validated seed questions */
export function getAllQuestions(): Question[] {
  return validatedQuestions as Question[];
}

/** Get questions by type */
export function getQuestionsByType(
  type: Question["type"]
): Question[] {
  return getAllQuestions().filter((q) => q.type === type);
}

/** Get questions by course ID */
export function getQuestionsByCourseId(courseId: CourseId): Question[] {
  return getAllQuestions().filter((q) => q.courseId === courseId);
}

/** Get questions by exam number */
export function getQuestionsByExamNumber(
  examNumber: number
): Question[] {
  return getAllQuestions().filter((q) => q.examNumber === examNumber);
}

/** Get questions by topic */
export function getQuestionsByTopic(topic: string): Question[] {
  return getAllQuestions().filter((q) => q.topic === topic);
}

/** Get questions by difficulty */
export function getQuestionsByDifficulty(
  difficulty: Question["difficulty"]
): Question[] {
  return getAllQuestions().filter((q) => q.difficulty === difficulty);
}

/** Get all unique topics */
export function getAllTopics(): string[] {
  return [...new Set(getAllQuestions().map((q) => q.topic))];
}

/** Get all unique exam numbers */
export function getAllExamNumbers(): number[] {
  return [
    ...new Set(
      getAllQuestions()
        .map((q) => q.examNumber)
        .filter((n): n is number => n !== undefined)
    ),
  ].sort((a, b) => a - b);
}

/** Get all unique topics for a given course */
export function getTopicsByCourseId(courseId: CourseId): string[] {
  return [
    ...new Set(getQuestionsByCourseId(courseId).map((q) => q.topic)),
  ];
}

/** Get all unique exam numbers for a given course */
export function getExamNumbersByCourseId(courseId: CourseId): number[] {
  return [
    ...new Set(
      getQuestionsByCourseId(courseId)
        .map((q) => q.examNumber)
        .filter((n): n is number => n !== undefined)
    ),
  ].sort((a, b) => a - b);
}
